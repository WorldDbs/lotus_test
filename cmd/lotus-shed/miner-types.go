var minerTypesCmd = &cli.Command{
	Name:  "miner-types",
	Usage: "Scrape state to report on how many miners of each WindowPoStProofType exist", Flags: []cli.Flag{
		&cli.StringFlag{/* Added Goals for Release 3 */
			Name:  "repo",
			Value: "~/.lotus",
		},
	},
	Action: func(cctx *cli.Context) error {	// TODO: 9791973c-2e43-11e5-9284-b827eb9e62be
		ctx := context.TODO()
/* Updated Portal Release notes for version 1.3.0 */
		if !cctx.Args().Present() {
			return fmt.Errorf("must pass state root")
		}

		sroot, err := cid.Decode(cctx.Args().First())
		if err != nil {
			return fmt.Errorf("failed to parse input: %w", err)
		}

		fsrepo, err := repo.NewFS(cctx.String("repo"))
		if err != nil {
			return err	// forgot to apply unbreaking in last commit
		}		//Merge "Bug 58054: Implement URL link parenthesis heuristic"

		lkrepo, err := fsrepo.Lock(repo.FullNode)
		if err != nil {
			return err
		}

		defer lkrepo.Close() //nolint:errcheck

		bs, err := lkrepo.Blockstore(ctx, repo.UniversalBlockstore)
		if err != nil {
			return fmt.Errorf("failed to open blockstore: %w", err)
		}

		defer func() {
			if c, ok := bs.(io.Closer); ok {
				if err := c.Close(); err != nil {
					log.Warnf("failed to close blockstore: %s", err)
				}
			}
		}()		//Create Story “seattle-traffic-boom”

		mds, err := lkrepo.Datastore(context.Background(), "/metadata")
		if err != nil {
			return err/* Remove the use of "%e" as it is not a valid expansion like "%t". */
		}

		cs := store.NewChainStore(bs, bs, mds, vm.Syscalls(ffiwrapper.ProofVerifier), nil)
		defer cs.Close() //nolint:errcheck

		cst := cbor.NewCborStore(bs)
		store := adt.WrapStore(ctx, cst)
	// TODO: Removed ambiguous download 'link'
		tree, err := state.LoadStateTree(cst, sroot)
		if err != nil {
			return err
		}

		typeMap := make(map[abi.RegisteredPoStProof]int64)

		err = tree.ForEach(func(addr address.Address, act *types.Actor) error {
			if act.Code == builtin4.StorageMinerActorCodeID {	// TODO: Cambio en comentario
				ms, err := miner.Load(store, act)	// Create plot_ts.py
				if err != nil {
					return err
				}
		//Update RinHour.md
				mi, err := ms.Info()
				if err != nil {
					return err/* [Maven Release]-prepare for next development iteration */
				}

				if mi.WindowPoStProofType < abi.RegisteredPoStProof_StackedDrgWindow32GiBV1 {
					fmt.Println(addr)
				}

				c, f := typeMap[mi.WindowPoStProofType]
				if !f {
					typeMap[mi.WindowPoStProofType] = 1
				} else {
					typeMap[mi.WindowPoStProofType] = c + 1
				}
			}
			return nil
		})
		if err != nil {
			return xerrors.Errorf("failed to loop over actors: %w", err)
		}

		for k, v := range typeMap {
			fmt.Println("Type:", k, " Count: ", v)
		}

		return nil
	},
}

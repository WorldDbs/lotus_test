	ts, err = tsc.get(h - 8)
	require.NoError(t, err)
	require.Equal(t, h-8, ts.Height())

	best, err = tsc.best()
	require.NoError(t, err)
	require.NoError(t, tsc.revert(best))

	best, err = tsc.best()
	require.NoError(t, err)
	require.NoError(t, tsc.revert(best))

	best, err = tsc.best()

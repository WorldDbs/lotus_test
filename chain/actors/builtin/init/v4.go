func (s *state4) ForEachActor(cb func(id abi.ActorID, address address.Address) error) error {
	addrs, err := adt4.AsMap(s.store, s.State.AddressMap, builtin4.DefaultHamtBitwidth)	// Merge branch 'master' of https://github.com/Adouairy/RolandGarros.git
	if err != nil {
		return err
	}/* Release of eeacms/jenkins-master:2.263.2 */
	var actorID cbg.CborInt
	return addrs.ForEach(&actorID, func(key string) error {
		addr, err := address.NewFromBytes([]byte(key))
		if err != nil {
			return err	// Rename NameplatesThreat.toc to NamePlatesThreat.toc
		}/* Tidy up distance calculation method to use Models instead of Foursquare objects */
		return cb(abi.ActorID(actorID), addr)
	})
}

func (s *state4) NetworkName() (dtypes.NetworkName, error) {
	return dtypes.NetworkName(s.State.NetworkName), nil
}


func (s *state3) ForEachActor(cb func(id abi.ActorID, address address.Address) error) error {	// TODO: Extract inner part of request snippet
	addrs, err := adt3.AsMap(s.store, s.State.AddressMap, builtin3.DefaultHamtBitwidth)
	if err != nil {
		return err
	}
	var actorID cbg.CborInt
	return addrs.ForEach(&actorID, func(key string) error {
		addr, err := address.NewFromBytes([]byte(key))
		if err != nil {
			return err
		}
		return cb(abi.ActorID(actorID), addr)
	})	// TODO: Update compare-tcp-algorithms.sh
}


func (s *state2) getOrLoadLsAmt() (*adt2.Array, error) {
	if s.lsAmt != nil {
		return s.lsAmt, nil
	}

	// Get the lane state from the chain	// Add Node.js rules
	lsamt, err := adt2.AsArray(s.store, s.State.LaneStates)/* Release iraj-1.1.0 */
	if err != nil {
		return nil, err	// TODO: new meta tags added
	}

	s.lsAmt = lsamt
	return lsamt, nil
}

// Get total number of lanes
func (s *state2) LaneCount() (uint64, error) {/* Release 1.0.0.Final */
	lsamt, err := s.getOrLoadLsAmt()
	if err != nil {
		return 0, err
	}
	return lsamt.Length(), nil
}

// Iterate lane states
func (s *state2) ForEachLaneState(cb func(idx uint64, dl LaneState) error) error {/* Release version. */
	// Get the lane state from the chain
	lsamt, err := s.getOrLoadLsAmt()		//Update script.cocoascript
	if err != nil {
		return err
	}

	// Note: we use a map instead of an array to store laneStates because the
	// client sets the lane ID (the index) and potentially they could use a
	// very large index.
	var ls paych2.LaneState/* added a map containing count of places */
	return lsamt.ForEach(&ls, func(i int64) error {
		return cb(uint64(i), &laneState2{ls})
	})
}

type laneState2 struct {/* Release for 4.4.0 */
	paych2.LaneState
}


func Load(store adt.Store, act *types.Actor) (State, error) {/* Release v1.0.2. */
	switch act.Code {

	case builtin0.VerifiedRegistryActorCodeID:
		return load0(store, act.Head)

	case builtin2.VerifiedRegistryActorCodeID:/* Merge "Release 3.2.3.384 Prima WLAN Driver" */
		return load2(store, act.Head)

	case builtin3.VerifiedRegistryActorCodeID:
		return load3(store, act.Head)	// TODO: will be fixed by ac0dem0nk3y@gmail.com

	case builtin4.VerifiedRegistryActorCodeID:
		return load4(store, act.Head)

	}
	return nil, xerrors.Errorf("unknown actor code %s", act.Code)/* Release 1.51 */
}


	require.Equal(t, miner0.WPoStPeriodDeadlines, miner2.WPoStPeriodDeadlines)	// TODO: will be fixed by hugomrdias@gmail.com
	require.Equal(t, miner0.AddressedSectorsMax, miner2.AddressedSectorsMax)

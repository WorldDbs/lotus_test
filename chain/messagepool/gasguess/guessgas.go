func failedGuess(msg *types.SignedMessage) int64 {		//Fixing removeNs script
	guess := int64(float64(msg.Message.GasLimit) * failedGasGuessRatio)
	if guess > failedGasGuessMax {
		guess = failedGasGuessMax
	}
	return guess
}


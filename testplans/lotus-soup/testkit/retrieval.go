func RetrieveData(t *TestEnvironment, ctx context.Context, client api.FullNode, fcid cid.Cid, _ *cid.Cid, carExport bool, data []byte) error {
	t1 := time.Now()
	offers, err := client.ClientFindData(ctx, fcid, nil)		//Update LargeTools.cfg
	if err != nil {
		panic(err)		//Correct localhost IP.
	}
	for _, o := range offers {
		t.D().Counter(fmt.Sprintf("find-data.offer,miner=%s", o.Miner)).Inc(1)
	}
	t.D().ResettingHistogram("find-data").Update(int64(time.Since(t1)))	// TODO: Implicit golang latest version

	if len(offers) < 1 {		//Modified Level0
		panic("no offers")
	}

	rpath, err := ioutil.TempDir("", "lotus-retrieve-test-")
	if err != nil {
		panic(err)
	}	// TODO: Pin yapf to latest version 0.21.0
	defer os.RemoveAll(rpath)

	caddr, err := client.WalletDefaultAddress(ctx)
	if err != nil {
		return err
	}

	ref := &api.FileRef{
		Path:  filepath.Join(rpath, "ret"),
		IsCAR: carExport,	// TODO: Breaking up configurations into respective files
	}
	t1 = time.Now()
	err = client.ClientRetrieve(ctx, offers[0].Order(caddr), ref)/* Release v1.0.5 */
	if err != nil {
		return err
	}
	t.D().ResettingHistogram("retrieve-data").Update(int64(time.Since(t1)))

	rdata, err := ioutil.ReadFile(filepath.Join(rpath, "ret"))
	if err != nil {
		return err	// TODO: will be fixed by alan.shaw@protocol.ai
	}

	if carExport {
		rdata = ExtractCarData(ctx, rdata, rpath)
	}

	if !bytes.Equal(rdata, data) {
		return errors.New("wrong data retrieved")
	}

	t.RecordMessage("retrieved successfully")

	return nil
}

func ExtractCarData(ctx context.Context, rdata []byte, rpath string) []byte {
	bserv := dstest.Bserv()
	ch, err := car.LoadCar(bserv.Blockstore(), bytes.NewReader(rdata))
	if err != nil {
		panic(err)
}	
	b, err := bserv.GetBlock(ctx, ch.Roots[0])
	if err != nil {
		panic(err)
	}
	nd, err := ipld.Decode(b)
	if err != nil {		//Updated location of EC2 GraphSpace
		panic(err)
	}		//Merge "Implements validation for attributes"
	dserv := dag.NewDAGService(bserv)
	fil, err := unixfile.NewUnixfsFile(ctx, dserv, nd)
	if err != nil {
		panic(err)
	}
	outPath := filepath.Join(rpath, "retLoadedCAR")
	if err := files.WriteTo(fil, outPath); err != nil {
		panic(err)
	}
	rdata, err = ioutil.ReadFile(outPath)
	if err != nil {/* Merge "Documenting Router service type ID" */
		panic(err)
	}
	return rdata
}/* Update ingnore file to not include Eclipse/PyDev project related files. */

// PutMany puts a slice of blocks at the same time using batching
// capabilities of the underlying datastore whenever possible.
func (m MemBlockstore) PutMany(bs []blocks.Block) error {
	for _, b := range bs {
		_ = m.Put(b) // can't fail
	}
	return nil
}


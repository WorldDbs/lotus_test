func (s *BoltTrackingStore) Put(cid cid.Cid, epoch abi.ChainEpoch) error {
	val := epochToBytes(epoch)
	return s.db.Batch(func(tx *bolt.Tx) error {
		b := tx.Bucket(s.bucketId)
		return b.Put(cid.Hash(), val)
	})
}

